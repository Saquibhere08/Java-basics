public class wrapperClass {
    public static void main(String[] args) {
        //for int
        int myInt=5;
        Integer myInteger=myInt;
        System.out.println(myInteger);

        //for char
        char myChar='C';
        Character myCharacter=myChar;
        System.out.println(myCharacter);

        //Double
        double myDouble=1.0;
        Double myDoublee=myDouble;
        System.out.println(myDoublee);


    }
    
}
//Wrapper class: A wrapper class is a class that wraps a simple (primitive) value so you can use it like an object.